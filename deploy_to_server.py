TARGET_DIR = "/root/vpn-bot"

def write_file(path, b64_data):
    raw = gzip.decompress(base64.b64decode(b64_data.replace("\n", "")))
    if os.path.exists(path):
        ts = datetime.datetime.now().strftime("%Y%m%d_%H%M%S")
        shutil.copy(path, f"{path}.bak_{ts}")
        print(f"  💾 backup: {path}.bak_{ts}")
    with open(path, "wb") as f:
        f.write(raw)
    print(f"  ✅ написан: {path} ({len(raw)} байт)")

print("=== Деплой VPN-бота ===")
